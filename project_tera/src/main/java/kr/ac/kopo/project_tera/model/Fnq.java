package kr.ac.kopo.project_tera.model;

public class Fnq {
	private int fnq;
	private String fnqName;
	private String fnqDate;
	private String fnqInfo;
	private String id;
	public int getFnq() {
		return fnq;
	}
	public void setFnq(int fnq) {
		this.fnq = fnq;
	}
	public String getFnqName() {
		return fnqName;
	}
	public void setFnqName(String fnqName) {
		this.fnqName = fnqName;
	}
	public String getFnqDate() {
		return fnqDate;
	}
	public void setFnqDate(String fnqDate) {
		this.fnqDate = fnqDate;
	}
	public String getFnqInfo() {
		return fnqInfo;
	}
	public void setFnqInfo(String fnqInfo) {
		this.fnqInfo = fnqInfo;
	}
	public String getid() {
		return id;
	}
	public void setid(String id) {
		this.id = id;
	}
}
